# How to add elements to dictionaries
""" 
created am empty dictionary called (d)
where we are going to add element
"""

d ={} # This is an empty dictionary

d[0] = "Welcome," # [0] is the key, "welcome" is the value
print(d)

d[1]= (" what is your name: ") # added a new element
print(d)

d["Name:"] = "Sam"  # dictionary with key as string
print(d)

