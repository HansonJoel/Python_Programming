"""
Append: The append method enables you insert element you wish to add to an existing list.

syntax: listname.append (a) 
where a is the parameter/element to be added to the list
Note: The append method parameter(a) only accepts one value
"""
num = [1,2,3,4]
print(num)
num.append(5)
print(num)

# You can also insert a string


num.append("are numbers")
print(num)
