# How to add element to a set
# SYNTAX: variablename.add(a)
# where a is the element to be added

set_numbers= {1,2,3,4}
print(set_numbers)

set_numbers.add(5)  # added 5 to the sequence of numbers
print(set_numbers)


# How to remove element from a set
# SYNTAX: variablename.remove(a)
# where a is the element to be added

set_numbers.remove(3)  #removes 3 from the sequence of numbers
print(set_numbers) 
